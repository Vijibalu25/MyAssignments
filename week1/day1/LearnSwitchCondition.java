package week1.day1;

public class LearnSwitchCondition {
	
	public static void main(String[] args) {
		
		String browser = "chrome";
		
		switch (browser)
		{
		case "chrome": 
			System.out.println("Chrome Broswer has opened succesfully");
			break;
		case "edge": 
			System.out.println("Edge Broswer has opened succesfully");
			break;
		}
	}

}
